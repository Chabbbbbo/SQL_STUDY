-- WHERE절 활용
SELECT *
	FROM EMP A
	WHERE A.SAL > 2000;


-- WHERE에 논리조건
SELECT *
	FROM EMP A
	WHERE A.SAL > 2000 
		OR A.HIREDATE > DATE'1982-01-01';

SELECT *
	FROM EMP A
	WHERE A.HIREDATE > DATE'1982-01-01'
		OR A.SAL > 2000;

SELECT *
	FROM EMP A
	WHERE A.HIREDATE > DATE'1982-01-01'
		OR A.SAL > 2000 
		AND A.JOB = 'CLERK';
    
SELECT *
	FROM EMP A
	WHERE A.HIREDATE > DATE'1982-01-01'
		OR (A.SAL > 2000 
		AND A.JOB = 'CLERK');
